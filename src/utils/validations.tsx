export const isNameValid = (name:String) => {
  if(name === "") {
    return false;
  }
  return true;
};

export const isEmailValid = (email) => {
  if(email === "") {
    return false;
  }
  return true;
}

export const isMobNoValid = (mobNo) => {
  if(mobNo === "") {
    return false;
  }
  return true;
}

export const isPasswordValid = (password) => {
  if(password === "") {
    return false;
  }
  return true;
}
